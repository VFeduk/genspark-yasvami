                logger.info(f"–û—á–∏—â–µ–Ω–æ {len(updates)} –Ω–∞–∫–æ–ø–∏–≤—à–∏—Ö—Å—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π")
